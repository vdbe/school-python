#!/usr/bin/env python3

print("digit: ", end="")
digit = input()

if digit == "0":
    print("nul")
elif digit == "1":
    print("een")
elif digit == "2":
    print("twee")
elif digit == "3":
    print("drie")
elif digit == "4":
    print("vier")
elif digit == "5":
    print("vijf")
elif digit == "6":
    print("zes")
elif digit == "7":
    print("zeven")
elif digit == "8":
    print("acht")
elif digit == "9":
    print("negen")
else:
    print(digit, "is not a digit")
